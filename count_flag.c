#include "ft_printf.h"

int		count_percent(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '%')
		{
			j++;
			if (j == 1)
			{
				if (str[i + 1] == 's' || str[i + 1] == 'S' || str[i + 1] == 'p' || str[i + 1] == 'd' || str[i + 1] == 'D' || str[i + 1] == 'i' || str[i + 1] == 'o' || str[i + 1] == 'O' || str[i + 1] == 'u' || str[i + 1] == 'U' || str[i + 1] == 'x' || str[i + 1] == 'X' || str[i + 1] == 'c' || str[i + 1] == 'C')
					j++;
			}
		}
		i++;
	}
	return (j);
}
